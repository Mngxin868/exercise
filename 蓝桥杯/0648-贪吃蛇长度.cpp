#include <iostream>
using namespace std;
int ans=0;
    string str = "+-------------------------------------------------+\
|                                                 |\
|    H######                      ####            |\
|          #                      #  #            |\
|          #                      #  #            |\
|          #     ####             #  #            |\
|          #     #  #             #  #            |\
|          ######@###             #  #            |\
|                #       ####     #  #            |\
|                #       #  #     #  #            |\
|            ####@#######@###     #  #            |\
|            #   #       #        #  #            |\
| T          #####       #        #  #   ##       |\
| #                      #      ###  ### ##       |\
| ################       #      #      ####       |\
|                #       #      #         #       |\
|   ##############       #######@##########       |\
|   #                         ###                 |\
|   ###########################                   |\
+-------------------------------------------------+";
//后面记得加\，不然系统无法判定后方空格是否是你定义的字符串。

int main()
{
    for(int i=0;i<str.size();i++){
      if(str[i]=='#'||str[i]=='H'||str[i]=='T')  ans++;//蛇头，蛇身，蛇尾加一；
      else if(str[i]=='@')  ans+=2;//重叠加二
    }
    cout<<ans<<endl;
  return 0;
}
